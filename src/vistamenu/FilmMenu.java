    public void cargarPeliculas(){
        DefaultTableModel modelo = (DefaultTableModel) tblFilm.getModel();
        modelo.setRowCount(0);
        rs = conexion.ConexionBD.Consulta("SELECT * FROM film WHERE title LIKE '"+txtB.getText()+"%' "
                + "OR language_name LIKE '"+txtB.getText()+"%'"
                + "OR category_name LIKE '"+txtB.getText()+"%'"
                + "OR rating LIKE '"+txtB.getText()+"%'");
        try {
            while (rs.next()){
                Vector v= new Vector();
                v.add(rs.getInt(1));
                v.add(rs.getString(2));
                v.add(rs.getString(3));
                v.add(rs.getDate(4));
                v.add(rs.getString(5));
                v.add(rs.getString(6));
                v.add(rs.getInt(7));
                v.add(rs.getDouble(8));
                v.add(rs.getString(9));
                v.add(rs.getString(10));
               
                modelo.addRow(v);
                tblFilm.setModel(modelo);
                
            }
        } catch (SQLException e) {
            
        }
    }

