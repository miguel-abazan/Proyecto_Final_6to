    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        // TODO add your handling code here:
         if(txtId.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Seleccione Registro a eliminar", "Error", JOptionPane.ERROR_MESSAGE);
        } else {
        int row = Integer.parseInt(txtId.getText());
        int opc = JOptionPane.showConfirmDialog(this, "¿Desea Eliminar De forma Permanente Este Registro?","Pregunta",JOptionPane.YES_NO_CANCEL_OPTION);
        if(opc == JOptionPane.YES_OPTION){
            try {
                FilmDAO.EliminarPelis(row);
                txtId.setText("");
                txtTit.setText("");
                txtDesc.setText("");
                cmbl1.setSelectedIndex(0);
                cml2.setSelectedIndex(0);
                txtDura.setText("");
                txtCos.setText("");
                cmbCla.setSelectedIndex(0);
                cmbCat.setSelectedIndex(0);
                txtTit.requestFocus();
                txtDesc.requestFocus();
                txtId.requestFocus();
                cal.setEnabled(true);
                JOptionPane.showMessageDialog(this, "Registro eliminado Correctamente","Aviso",JOptionPane.INFORMATION_MESSAGE);
            } catch (SQLException e) {
            }
        }
        }
        cargarPeliculas();
    }//GEN-LAST:event_jButton3ActionPerformed

