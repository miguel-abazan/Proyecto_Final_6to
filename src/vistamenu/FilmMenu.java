    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(txtTit.getText().isEmpty()||txtDesc.getText().equals("") || cmbl1.getSelectedIndex()==0 || cml2.getSelectedIndex()==0 || txtDura.getText().equals("")
                || txtCos.getText().equals("")|| cmbCla.getSelectedIndex()==0|| cmbCat.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(this, "Ingrese los datos Correctamente", "Error", JOptionPane.ERROR_MESSAGE);
            
        } else {
            rs =conexion.ConexionBD.Consulta("SELECT COUNT(title) FROM film WHERE title= '"+ txtTit.getText()+ "'");
            try {
                while(rs.next()){
                    cont = rs.getInt(1);
                }
                    
            } catch (SQLException e) {
            }
            if(cont >= 1){
                JOptionPane.showMessageDialog(this, "Esta Pelicula Ya esta en La BASE DE DATOS","Informacion",JOptionPane.INFORMATION_MESSAGE);
            } else{
                ConexionBD cc= new ConexionBD();
                Connection cn = cc.getConnection();
                try {
                    PreparedStatement pst = cn.prepareStatement("INSERT INTO film(title,description,release_year,language_name, "
                            + "original_language_name ,length,cost,rating,category_name) VALUES (?,?,?,?,?,?,?,?,?)");
                    pst.setString(1, txtTit.getText());
                    pst.setString(2, txtDesc.getText());
                    pst.setDate(3, new java.sql.Date(cal.getDate().getTime()));
                    pst.setString(4,String.valueOf(cmbl1.getSelectedItem()));
                    pst.setString(5,String.valueOf(cml2.getSelectedItem()));
                    pst.setInt(6, Integer.parseInt(txtDura.getText()));
                    pst.setDouble(7, Double.parseDouble(txtCos.getText()));
                    pst.setString(8,String.valueOf(cmbCla.getSelectedItem()));
                    pst.setString(9,String.valueOf(cmbCat.getSelectedItem()));
                    
                    pst.executeUpdate();
                    
                } catch (SQLException ex) {
                    Logger.getLogger(FilmMenu.class.getName()).log(Level.SEVERE, null, ex);
                }
                JOptionPane.showMessageDialog(this, "LOS DATOS SE REGISTRARON CORRECTAMENTE");
                txtTit.setText("");
                txtDesc.setText("");
                cmbl1.setSelectedIndex(0);
                cml2.setSelectedIndex(0);
                txtDura.setText("");
                txtCos.setText("");
                cmbCla.setSelectedIndex(0);
                cmbCat.setSelectedIndex(0);
                
            }
       }
        cargarPeliculas();
            
     
    }//GEN-LAST:event_jButton1ActionPerformed

